package dto;

public class Bike {
    private int bikeId;
    private String bikeName;
    private String bileBrand;
    private double bikecc;
    private double bikePrice;
    private String bikeColor;
	public int getBikeId() {
		return bikeId;
	}
	public void setBikeId(int bikeId) {
		this.bikeId = bikeId;
	}
	public String getBikeName() {
		return bikeName;
	}
	public void setBikeName(String bikeName) {
		this.bikeName = bikeName;
	}
	public String getBileBrand() {
		return bileBrand;
	}
	public void setBileBrand(String bileBrand) {
		this.bileBrand = bileBrand;
	}
	public double getBikecc() {
		return bikecc;
	}
	public void setBikecc(double bikecc) {
		this.bikecc = bikecc;
	}
	public double getBikePrice() {
		return bikePrice;
	}
	public void setBikePrice(double bikePrice) {
		this.bikePrice = bikePrice;
	}
	public String getBikeColor() {
		return bikeColor;
	}
	public void setBikeColor(String bikeColor) {
		this.bikeColor = bikeColor;
	}
    
}
